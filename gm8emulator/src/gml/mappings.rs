#[inline(always)]
pub fn find_function_by_address<T>(func: &gml::FunctionPtr<T>) -> Option<(usize, &str)> {
    FUNCTIONS.entries().enumerate()
        .find(|(_, (_, &v))| v.addr() == std::ptr::from_ref(&func.0) as *const ())
        .map(|(i, (&k, _))| (i, k))
}
